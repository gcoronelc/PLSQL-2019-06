-- ARRAYAS ASOCIATIVOS

-- EJEMPLO 1

DECLARE
	TYPE  ARRAY_NOTAS IS TABLE OF NUMBER INDEX BY BINARY_INTEGER;
	NOTAS ARRAY_NOTAS;
BEGIN
	-- CARGAR NOTAS
	NOTAS(1) := 20;
	NOTAS(2) := 18;
	NOTAS(3) := 15;
	NOTAS(4) := 17;
	-- MOSTRAR NOTAS
	DBMS_OUTPUT.PUT_LINE('TAMAÑO: ' || NOTAS.COUNT);
	FOR I IN 1..NOTAS.COUNT LOOP
		DBMS_OUTPUT.PUT_LINE('NOTA ' || I || ': ' || NOTAS(I));
	END LOOP;
END;


